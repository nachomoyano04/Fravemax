    private void tfPrecioKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_tfPrecioKeyTyped
        int key = evt.getKeyChar();
        boolean esNum = (key >= 48) && (key<=57); 
        if(!esNum){
            evt.consume();
        }
        if(tfPrecio.getText().trim().length()>=10){
            evt.consume();
        }
    }//GEN-LAST:event_tfPrecioKeyTyped

    private void tableProductosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableProductosMouseClicked
        int fila = tableProductos.getSelectedRow();
        if(estado.equalsIgnoreCase("modificacion")){
            tfDescripcion.setEditable(true);
            tfPrecio.setEditable(true);
            tfStock.setEditable(true);
            tfDescripcion.setText(table.getValueAt(fila, 1)+"");
            tfPrecio.setText(table.getValueAt(fila, 2)+"");
            tfStock.setText(table.getValueAt(fila, 3)+"");
            if((table.getValueAt(fila, 4)+"").equalsIgnoreCase("Activo")){
                rbActivo.setSelected(true);
            }else{
                rbInactivo.setSelected(false);
            }
        }
    }//GEN-LAST:event_tableProductosMouseClicked

