    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        if(verificarCampos()){
            Producto pr = new Producto();
            pr.setDescripcion(tfDescripcion.getText());
            pr.setPrecioActual(BigDecimal.valueOf(Double.parseDouble(tfPrecio.getText())));
            pr.setStock(Integer.parseInt(tfStock.getText()));
            if(rbActivo.isSelected()){
                pr.setEstado(1);
            }else{
                pr.setEstado(0);
            }
            if(estado.equalsIgnoreCase("nuevo")){
                pd.nuevoProducto(pr);
                llenarTablaProductos();
                limpiar();
            }else{
                pr.setIdProducto(Integer.parseInt(table.getValueAt(tableProductos.getSelectedRow(), 0)+""));
                pd.modificarProducto(pr);
                llenarTablaProductos();
                limpiar();
            }
        }else{
            JOptionPane.showMessageDialog(null, "Hay campos sin rellenar");
        }
    }//GEN-LAST:event_btnGuardarActionPerformed

