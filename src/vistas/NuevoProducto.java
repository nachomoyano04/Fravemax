    private void llenarTablaProductos() {
        bg.add(rbActivo);
        bg.add(rbInactivo);
        table = (DefaultTableModel) tableProductos.getModel();
        table.setRowCount(0);
        ArrayList<Producto>productos = pd.listarProductos();
        if(!productos.isEmpty()){
            for (Producto p : productos) {
                String estado = "";
                if(p.getEstado() == 1){
                    estado = "Activo";
                }else{
                    estado = "Inactivo";
                }
                table.addRow(new Object[]{p.getIdProducto(), p.getDescripcion(), p.getPrecioActual(), p.getStock(), estado});
            }
        }else{
            JOptionPane.showMessageDialog(this,"Aún no hay productos registrados");
        }
    }

    private void limpiar() {
        tableProductos.clearSelection();
        rbActivo.setSelected(true);
        tfDescripcion.setText("");
        tfPrecio.setText("");
        tfStock.setText("");
        btnModificarProducto.setEnabled(true);
        labelTitle.setText("Nuevo Producto");
        labelSeleccioneFila.setVisible(false);
        estado = "nuevo";
        tfDescripcion.setEditable(true);
        tfPrecio.setEditable(true);
        tfStock.setEditable(true);
    }

    private boolean verificarCampos() {
        String descripcion = tfDescripcion.getText();
        String precio = tfPrecio.getText();
        String stock = tfStock.getText();
        return ((!descripcion.isEmpty()) && (!precio.isEmpty()) && (!stock.isEmpty()));
    }
